package ru.mirea.Lab2;

public class Circle {
    double x0, y0, R;

    public double getX0() {
        return x0;
    }

    public double getY0() {
        return y0;
    }

    public double getR() {
        return R;
    }

    public void setR(double r) {
        R = r;
    }

    public void setY0(double y0) {
        this.y0 = y0;
    }

    public void setX0(double x0) {
        this.x0 = x0;
    }
}